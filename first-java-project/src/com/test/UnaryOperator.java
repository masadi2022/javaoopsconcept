package com.test;

public class UnaryOperator {

	public static void main(String[] args) {
		
		int noOfApples=10;
		
		//post decrement
		System.out.println(noOfApples--);
		System.out.println(noOfApples);
		
		//pre decrement
		System.out.println(--noOfApples);
		System.out.println(noOfApples);
		
		//post increment
		System.out.println(noOfApples++);
		System.out.println(noOfApples);
		
		//pre increment
		System.out.println(--noOfApples);
		System.out.println(noOfApples);
	}

}
